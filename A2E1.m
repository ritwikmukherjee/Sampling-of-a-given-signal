fm=120;
T=1/fm;
t=0:0.0001:2*T;
x=5*cos(2*fm*pi*t);
subplot(2,2,1);
plot(t,x);
xlabel('Time');
ylabel('Amplitude');
title('Original');
fs1=80;
fs2=400;
fs3=1000;
subplot(2,2,2);
n1=0:1/fs1:2*T;
y2=5.*cos(240*pi.*n1);
stem(n1,y2);
xlabel('n');
ylabel('Amplitude');
title('Sample1');
hold on;
plot(t,x,'r');
subplot(2,2,3);
n2=0:1/fs2:2*T;
y3=5.*cos(240*pi.*n2);
stem(n2,y3);
xlabel('n');
ylabel('Amplitude');
title('Sample2');
hold on;
